SELECT NOME FROM PRODUTO WHERE COD_PROD IN
(SELECT COD_PROD FROM ITEM_VENDA WHERE QUANT_VEN IN
(SELECT MAX(QUANT_VEN) FROM ITEM_VENDA WHERE COD_VEN IN
(SELECT COD_VEN FROM VENDA WHERE DT_VEND>='2023-01-01' AND DT_VEND<='2023-01-31')));
