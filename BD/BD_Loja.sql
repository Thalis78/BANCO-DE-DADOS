--OBTER O NOME DOS CLIENTES QUE NÃO FORAM ATENDIDOS PELO VENDEDOR ROBERTO:
SELECT NOME FROM CLIENTE WHERE COD_CLI NOT IN
(SELECT COD_CLI FROM VENDA WHERE COD_VEND IN
(SELECT COD_VEND FROM VENDEDOR WHERE NOME='ROBERTO'));
